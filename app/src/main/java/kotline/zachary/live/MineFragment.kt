class MineFragment : Fragment(){
    lateinit var ivHeader:CircleImageView
    lateinit var tvLogout:TextView
    override fun onCreateView(inflater: LayoutInflater, container: ViewGroup?, savedInstanceState: Bundle?): View? {
        val v: View = inflater.inflate(R.layout.fg_mine,null)
        initView(v)
        return v
    }

    private fun initView(v: View) {
        ivHeader = v.findViewById(R.id.ivHeader)
        tvLogout = v.findViewById(R.id.tvLogout)
        ivHeader.setOnClickListener {
            slectImage()
        }
        tvLogout.setOnClickListener {
            startActivity(Intent(activity,TestActivity::class.java))
        }
    }

    private fun slectImage() {
        Album.image(activity)
            .singleChoice()
            .camera(true)
            .columnCount(3)
            .onResult { result ->
                var path:String = result[0].path
                Glide.with(activity).load(path).into(ivHeader)
            }
            .onCancel {

            }
            .start()
    }

    companion object {
        fun getInstance(): MineFragment {
            return MineFragment()
        }
    }
}