class CloudFragment : Fragment(){
    override fun onCreateView(inflater: LayoutInflater, container: ViewGroup?, savedInstanceState: Bundle?): View? {
        val v: View = inflater.inflate(R.layout.fg_cloud,null)
        initView(v)
        return v
    }

    private fun initView(v: View) {
        //轮播图
        var mBanner : Banner = v?.findViewById(R.id.banner) as Banner
        var images : ArrayList<String> = ArrayList()
        for (j in 0..6){
            images.add("https://timgsa.baidu.com/timg?image&quality=80&size=b9999_10000&sec=1544070168104&di=170d339180f9b9a4271010c3879e1a58&imgtype=0&src=http%3A%2F%2Fimg.mp.itc.cn%2Fupload%2F20170523%2F13fe8ccf2c034abb8b077d211e613980_th.jpg")
        }
        mBanner.setImages(images).setImageLoader(GlideImageLoader()).start()
        mBanner.setOnBannerListener { p->
            Toast.makeText(activity,"banner:"+p, Toast.LENGTH_SHORT).show()
        }

    }

    companion object {
        fun getInstance(): CloudFragment {
            return CloudFragment()
        }
    }
}