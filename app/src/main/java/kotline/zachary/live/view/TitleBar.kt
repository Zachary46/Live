public class TitleBar{
    private var tvTitle: TextView? =null
    private var tvBack:  TextView? =null
    private var ivBack:  ImageView? =null
    constructor(activity: AppCompatActivity, header: View){
        ivBack = header.findViewById<ImageView>(R.id.ivBack)
        tvTitle = header.findViewById<TextView>(R.id.tvTitle)
        tvBack = header.findViewById<TextView>(R.id.tvBack)
        ivBack!!.setOnClickListener {
            activity.finish()
        }
    }

    fun setTitle(text: String): TitleBar {
        tvTitle?.run {
            setText(text)
        }
        return this
    }

    fun setBackText(text: String): TitleBar {
        tvBack?.run {
            setText(text)
        }
        return this
    }

    fun setBackIcon(res: Int): TitleBar {
        ivBack?.run {
            setImageResource(res)
        }
        return this
    }

    fun hideBackIcon(): TitleBar{
        ivBack?.visibility=View.GONE
        return this
    }

    fun showBackIcon(): TitleBar{
        ivBack?.visibility=View.VISIBLE
        return this
    }
}